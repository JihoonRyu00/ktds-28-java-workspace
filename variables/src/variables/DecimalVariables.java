package variables;

public class DecimalVariables {
	public static void main(String[] args) {
		// 정수 변수 생성
		// byte, short, int, long
		byte byteNumber = -128; // 1 byte,256,-128~127
		short shortNumber = 3_300; // 2 bytes, 대략 -32,000~32,000
		int intNumber = 300_000_000; // 4bytes, 대략 -21억~21억
		long longNumber = 4_000_000_000L; // 8bytes. 대략 -900경~900경
	}
}
